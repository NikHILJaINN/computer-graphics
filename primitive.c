#include <GL/glut.h>
void display()
{
glClear(GL_COLOR_BUFFER_BIT);
glColor3f(0,1,0);
glBegin(GL_QUADS);
glVertex2f(-0.2,0.2);
glVertex2f(0,0.2);
glVertex2f(0,0);
glVertex2f(-0.2,0);
glEnd();
glColor3f(1,1,0);
glRasterPos2f(-0.2,-0.1);
glutBitmapString(GLUT_BITMAP_HELVETICA_18,"square");
glColor3f(0,0,1);
glBegin(GL_POLYGON);
glVertex2f(-0.7,0.2);
glVertex2f(-0.6,0.2);
glVertex2f(-0.5,0.1);
glVertex2f(-0.6,0);
glVertex2f(-0.7,0);
glVertex2f(-0.8,0.1);
glEnd();
glColor3f(1,1,0);
glRasterPos2f(-0.8,-0.1);
glutBitmapString(GLUT_BITMAP_HELVETICA_18,"hexagon");
glColor3f(0,1,0);
glBegin(GL_POINTS);
glVertex2f(0.2,0.2);
glVertex2f(0.4,0.2);
glVertex2f(0.4,0);
glVertex2f(0.2,0);
glEnd();
glColor3f(1,1,0);
glRasterPos2f(0.2,-0.1);
glutBitmapString(GLUT_BITMAP_HELVETICA_18,"points");
glColor3f(1,1,0);
glBegin(GL_LINES);
glVertex2f(0.6,0.2);
glVertex2f(0.6,0);
glVertex2f(0.65,0.2);
glVertex2f(0.65,0);
glEnd();
glColor3f(1,1,0);
glRasterPos2f(0.6,-0.1);
glutBitmapString(GLUT_BITMAP_HELVETICA_18,"lines");
glColor3f(1,0,1);
glBegin(GL_LINE_STRIP);
glVertex2f(-0.8,-0.3);
glVertex2f(-0.55,-0.3);
glVertex2f(-0.4,-0.35);
glVertex2f(-0.7,-0.35);
glVertex2f(-0.6,-0.4);
glVertex2f(-0.8,-0.4);
glEnd();
glColor3f(1,1,0);
glRasterPos2f(-0.8,-0.5);
glutBitmapString(GLUT_BITMAP_HELVETICA_18,"line strip");
glColor3f(0,1,1);
glBegin(GL_LINE_LOOP);
glVertex2f(-0.3,-0.3);
glVertex2f(-0.3,-0.4);
glVertex2f(0,-0.4);
glVertex2f(0,-0.3);
glEnd();
glColor3f(1,1,0);
glRasterPos2f(-0.3,-0.5);
glutBitmapString(GLUT_BITMAP_HELVETICA_18,"line loop");
glFlush();
}
int main(int argc,char** argv)
{
glutInit(&argc,argv);
glutInitDisplayMode(GLUT_SINGLE|GLUT_RGB);
glutInitWindowSize(900,900);
glutInitWindowPosition(300,500);
glutCreateWindow("PRIMITIVE TYPES");
glClearColor(0,0,0,1);
glutDisplayFunc(display);
glutMainLoop();
return 0;
}
